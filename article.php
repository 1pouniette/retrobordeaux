			<br><h1>Oubliez votre âge au salon du vintage !</h1><br>
			
			<h5>Dans le hangar 14, au milieu de la modernité du quai des marques, le Salon du Vintage a ouvert ses portes le 9 et 10 mars dernier. Ce ralliement des époques invite à (re)découvrir des objets du quotidien, de l’ameublement à l’automobile, en passant par la mode ou la musique. Pourquoi un tel engouement ? Eléments de réponse ci-dessous.</h5><br>
			
			<img src="img/salon-du-vintage.jpg" class="card-img" alt="Exposition au salon du vintage.">
			
			<br><br><p>Difficile de ne pas prendre “un coup de vieux” quand un enfant vous demande interloqué, à quoi ressemble une disquette ? Pas de panique, vous avez enfin l’occasion de retrouver les objets si chers à votre enfance, ou de découvrir celle de vos parents !<br> <br> 
 
			Pour cette nouvelle édition, le salon vous accueille : collectionneurs, nostalgiques, ou simplement désireux de découvrir les tendances d'antan. Avec ses 3000m² de vêtements, vous trouverez votre bonheur. Tenues de grands couturiers comme Saint Laurent ou Dior, bons plans dans l'espace Frip Chic, mais aussi des meubles, objets de collection ou vinyles se partagent les allées avec la foule. Pas de moderne, tous ces objets datent des années 50 aux années 80.<br> <br> 

			Cette année, le salon met en avant les objets iconiques. Le minitel, le Rubik’s cube, les consoles Atari, les chaises Panton ou encore le walkman ne vous rappellent-ils pas des souvenirs ? Ils sont entrés dans les moeurs grâce à leur design innovant, leur concept révolutionnaire ou simplement leur utilité. Le salon du vintage vous les a retrouvés pour une exposition hommage ! Vous avez donc le plaisir de revivre les années 70 avec les lignes space, les couleurs flashy des années 80 et le formalisme des années 90.<br> <br> 

			Ce salon n'est pas uniquement un lieu de vente, vous y retrouvez de nombreux stands spéciaux. Les tatoueurs et "barbershop", les salons de coiffure, de maquillage vintage et les habilleurs sont prêts à vous téléporter dans le temps. Pendant ce temps, les enfants ont accès à un espace "rétrogaming" leur permettant de découvrir les jeux vidéo à l'époque de leur parents.
			</p>
			
			<br><h4>Le rétro, pas seulement une tendance.</h4><br>
			
			<p>La tendance rétro est partout : que ce soit dans nos maisons ou même dans notre garde-robe, elle ne nous quitte plus. Issu des années 60, ce courant connaît un succès fulgurant depuis ces dernières années. D’où vient cette popularité ? La génération Y, située entre les années 1980 et 2000, est particulièrement touchée par cette tendance. <br> <br> 

			En effet, notre monde moderne en plein accroissement berce les millenials. Par manque de repères, cette génération se rassure en se rattachant au passé. Autrefois abandonné pour des raisons d’amélioration vers un monde moderne, le vintage est aujourd’hui sollicité et idéalisé. Les initiés s’approprient le look du passé, pour le remettre au goût du jour.<br> <br> 

			Le vintage permet une forme d’indépendance face à l’industrie de la mode. Avec les réseaux sociaux, l’uniformisation est un phénomène courant. Les vêtements de seconde-main encouragent donc l’individualité. Chaque individu devient son propre designer de tenues personnalisées. Cette génération trouve alors à travers cette tendance un moyen de se distinguer et de s’affirmer dans une société trop standardisée.
			</p>
			
			<br><h4>“C’était mieux avant !”</h4><br>
			
			<p>Le vintage développe une certaine fascination pour les objets du passé, que cela soit en ameublement, automobile ou encore mode et beauté.  Reprendre les succès de chaque décennie et les transposer au goût du jour permet de se réapproprier une époque face à un futur incertain.<br><br>  

			Qui n’a jamais économisé en achetant les meubles du voisin au vide-grenier, trouvé une pépite dans une brocante ou une pièce rare dans une friperie ? Le vintage est moins cher et se démocratise à travers les générations.<br> <br> 

			L’avantage de consommer vintage est de pouvoir échanger, récupérer et réutiliser des objets. Cette vieille console que quelqu’un jetterait à la déchetterie ferait certainement le bonheur d’un collectionneur, attirait l’attention d’un enfant ou rendrait des parents nostalgiques. La réparation et la restauration appartiennent aussi à l’économie du vintage. Un coup de peinture sur le meuble de grand-père et il est comme neuf ! Une visite chez le garagiste et votre 2 CV repart comme sur des roulettes !<br> <br> 

			En plus d’être économique, le vintage est écologique. En effet, la réparation et la réutilisation permet de donner une seconde vie à des objets. L’achat est plus durable et s’oppose aux problèmes de consommation moderne tels que l’obsolescence programmée.
			</p>
			
			<h6>Le vintage suscite un véritable engouement au fur et à mesure des années et restera un phénomène intergénérationnel indémodable. Dans un monde ultra-normalisé, le vintage permet aux gens de se reconnecter avec le passé et de se démarquer en portant des pièces uniques. Alors n'oubliez pas de garder vos vieux vêtements ou les meubles qui vous semblent démodés, ils feront peut-être le bonheur de quelqu’un dans quelques années. Comme le dit Laurent Journo, créateur du salon du vintage, “Les créations d’aujourd’hui sont le vintage de demain”. </h6>
			
